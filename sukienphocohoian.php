        <b>Phố Cổ Hội An</b><br><br>
        Phố Hội An là mẫu hình tiêu biểu về một cảng thị truyền thống Đông Nam Á trong giai đoạn thế kỷ 15 – 16. Cảng thị này có mầm mống sơ khai từ trước Công nguyên với nền văn hóa Sa Huỳnh được tiếp tục phát triển dưới thời Chăm (thế kỷ 2 sau CN – thế kỷ 15) và cực thịnh trong thời Đại Việt, Đại Nam (thế kỷ 15 – thế kỷ 19). Bên cạnh các di tích, di chỉ khảo cổ có niên đại trên 2000 – 3000 năm là một số lượng lớn di tích kiến trúc, chủ yếu làm bằng gỗ, có niên đại phổ biến từ thế kỷ 17 – 19. <br>
        Năm 1999, đã có 1360 di tích, danh thắng với nhiều loại hình được thống kê. Đó là những đình, chùa, lăng, miếu, mộ, cầu, giếng, nhà thờ tộc, thánh thất, hội quán, nhà ở … phân bố theo những trục đường truyền thống nhỏ hẹp, vừa mang đậm sắc thái địa phương Việt Nam, vừa thể hiện rõ sự giao lưu hội nhập văn hóa mạnh mẽ với các nước phương Đông và phương Tây. Liên tục trong nhiều thế kỷ, những giá trị văn hóa truyền thống của nhân dân Hội An cùng với phong tục tập quán, các sinh hoạt, vui chơi, giải trí, cũng như các món ăn xưa vẫn được giữ gìn, và phát huy.<br>
