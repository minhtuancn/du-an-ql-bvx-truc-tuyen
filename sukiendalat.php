        <b>Đà Lạt</b><br><br>
        Được người Pháp phát hiện từ những năm cuối thế kỷ 19 và hình thành vào những năm đầu thế kỷ 20, Đà Lạt là một thành phố nghỉ dưỡng trẻ trung với nhiều ưu thế tự nhiên về khí hậu và cảnh quan. Nhờ vào độ cao 1.475m so với mặt biển nên dù là một xứ nhiệt đới, Đà Lạt có được một khí hậu mát mẻ, dễ chịu của vùng ôn đới với nhiệt độ trung bình trong ngày thấp nhất là 15oC và cao nhất là 24oC.<br>
        Mặc dù có hai mùa: mùa mưa từ tháng 4 đến tháng 11 và mùa nắng từ tháng 12 đến tháng 3 năm sau nhưng quanh năm Đà Lạt đều có nắng. Các nhà khí hậu học quả không quá lời khi gọi Đà Lạt là thành phố của mùa xuân. Hồ Xuân Hương nằm giữa được coi là trung tâm của thành phố, ở độ cao 1.475m. Vì vậy Đà Lạt gần có dạng lòng chảo hình bầu dục.<br>
