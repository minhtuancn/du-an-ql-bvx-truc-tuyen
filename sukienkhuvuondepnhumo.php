        <h4><b>Ngắm khu vườn đẹp như trong mơ</b></h4><br>
        <b>&nbsp;&nbsp;Vườn Longwood nằm trong thung lũng Brandywine Creek thuộc bang Pennsylvania là một trong những khu vườn lớn và đẹp nhất nước Mỹ.</b><br><br>
        &nbsp;&nbsp;Khu vườn bao phủ diện tích hơn 4,2km2 này có cảnh quan rất đa dạng: Từ những khu vườn, đồng cỏ xanh mướt mắt cho đến những cánh rừng yên ả, thanh bình. Tới nơi đây, bạn sẽ không chỉ được ngắm nhìn những loài thực vật kỳ lạ mà còn được chiêm ngưỡng nghệ thuật trồng hoa độc đáo.<br><br>
        &nbsp;&nbsp;Ngoài ra, hằng năm có tới 800 sự kiện văn hóa như triển lãm hoa, biểu diễn nhạc nước, hòa nhạc hay bắn pháo hoa được tổ chức tại Longwood. Hai mươi vườn cây ngoài trời và 20 vườn cây trong nhà kính là nơi bảo tồn 11.000 loài cây, hoa khác nhau trên khắp thế giới.<br>
        &nbsp;&nbsp;Có thể nói nhà kính của Longwood là một trong những nhà kính lớn nhất thế giới, chứa 5.500 loài thực vật. Khu vườn mở cửa cho du khách tham quan quanh năm và là điểm đến hấp dẫn không thể bỏ qua. Chỉ tính riêng trong năm 2009, khu vườn đã thu hút gần 900.000 du khách.<br>
