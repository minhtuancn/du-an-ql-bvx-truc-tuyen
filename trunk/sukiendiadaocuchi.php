        <b>Địa đạo củ chi</b><br><br>
        Thăm địa đạo Củ Chi. Là một hệ thống đường hầm sâu dưới đất từ 3 – 8m, chiều cao chỉ đủ cho một người đi lom khom. Căn hầm đầu tiên ở ngay bìa rừng có giếng ngầm cung cấp nguồn nước uống và sinh hoạt cho toàn khu vực địa đạo.<br>
        Giếng sâu 15m, trong vắt. Hệ thống địa đạo gồm 3 tầng, từ đường "xương sống" toả ra vô số nhánh dài, nhánh ngắn ăn thông nhau, có nhánh trổ ra tận sông Sài Gòn. Tầng một cách mặt đất 3m, chống được đạn pháo và sức nặng của xe tăng, xe bọc thép. Tầng 2 cách mặt đất 5m, có thể chống được bom cỡ nhỏ. Còn tầng cuối cùng cách mặt đất 8-10m hết sức an toàn. Ðường lên xuống giữa các tầng hầm được bố trí bằng các nắp hầm bí mật. Bên trên ngụy trang kín đáo, nhìn như những ụ mối đùn, dọc đường hầm có lỗ thông hơi.<br>
