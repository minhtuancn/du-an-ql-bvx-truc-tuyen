        Ðiều thú vị nữa khi về thăm mảnh đất anh hùng này là các hướng dẫn viên đều ăn mặc như hồi chiến tranh: nam mặc đồ giải phóng quân, mũ tai bèo; nữ mặc bà ba đen, quàng khăn rằn và đi dép lốp. Sự có mặt của họ bên trong những bụi cây, ở mỗi khúc quanh khiến du khách có cảm giác đang được sống trong sự thật của Củ Chi thời đánh giặc. Cảm giác thời chiến rõ nét hơn khi đĩa sắn luộc, bát muối vừng được đưa mời khách du lịch. Những ly rượu bé xíu, trong vắt và nồng nàn được gọi đùa là nước mắt quê hương bên đĩa rau mốp muối chua, giòn và ngon đến lạ. Bạn đừng quên nếm thử món rau mốp. Rau mốp là lương thực chính của quân dân Củ Chi ngày trước, còn giờ đây đang là đặc sản.<br>
