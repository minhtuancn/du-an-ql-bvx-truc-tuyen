        <b>Cố đô Huế</b><br><br>
        Tỉnh Thừa Thiên - Huế là tỉnh thuộc dải đất Miền Trung của Việt Nam. Thừa Thiên Huế có diện tích rộng 5.900 km2 với dân số trên 1 triệu người (1.064.000 người - số liệu năm 2000). Tỉnh Thừa Thiên Huế có các huyện Phong Điền, Quảng Điền, Hương Trà, Phú Vang, Hương Thủy, Phú Lộc, Nam Đông, A Lưới và thành phố Huế.<br>
        Huế là một trong những vùng có nhiều di sản văn hoá. Đến nay, không còn một vùng nào có một số lượng lớn các di tích mà những di tích này vẫn giữ được hình dạng vốn có của nó như ở cố đô này. Ở bờ phía Bắc của sông Hương là một di tích gồm những lâu đài được xây dựng theo kiểu phòng thủ tạo thành một đường vòng cung dài 11Km. Công trình quí giá này gồm hơn một 100 tác phẩm kiến trúc, đã thực sự phản ánh được cuộc sống của vua quan nhà Nguyễn. Giữa những quả đồi ở bờ Nam sông Hương là những lăng tẩm rất đẹp của các vua Nguyễn. Trong số đó nổi tiếng nhất là bốn lăng tẩm mà mỗi lăng được biết đến với cái tên phù hợp với tính cách của mỗi vua và kiểu kiến trúc của mỗi lăng. Đó là lăng Gia Long uy nghi, lăng Minh Mạng oai phong, lăng Tự Đức thơ mộng và lăng Khải Định tráng lệ.<br>
        Huế đồng thời còn là một trung tâm quan trọng của Đạo Phật. ở Huế và những vùng lân cận vẫn còn tồn tại hàng chục chùa đã được xây dựng cách đây trên 300 năm và hàng trăm đền, chùa được xây dựng đầu thế kỷ.<br>
        Ngoài ra, Huế còn được xem như là nơi bắt nguồn của nhạc cung đình, là nơi có nhiều món ăn truyền thống nổi tiếng và nghề thủ công tinh xảo.
Là thành phố duy nhất trong nước vẫn còn giữ được dáng vẻ của một thành phố thời Trung cổ và nguyên vẹn kiến trúc của một nền quân chủ. Huế đã trở thành một bảo tàng lớn và vô giá. Chính vì vậy, chính phủ đã xếp hạng các di tích ở cố đô Huế như là một tài sản vô cùng quí giá và tháng 12 năm 1993 Huế đã được UNESCO xếp hạng là di tích văn hóa thế giới.<br>
